    private void preencherTabela() {
        
        DefaultTableCellRenderer cellRender = new DefaultTableCellRenderer();
        tabLote.getColumnModel().getColumn(0).setPreferredWidth(100);  //Preferencia de largura medidad em pixels
        tabLote.getColumnModel().getColumn(1).setPreferredWidth(100);  
        tabLote.getColumnModel().getColumn(2).setPreferredWidth(75);  
        tabLote.getColumnModel().getColumn(3).setPreferredWidth(100);
        tabLote.getColumnModel().getColumn(4).setPreferredWidth(100);
        for (int i=0; i<tabLote.getColumnCount(); i++)
            tabLote.getColumnModel().getColumn(i).setCellRenderer(cellRender);
        
        DefaultTableModel modelo = (DefaultTableModel) tabLote.getModel();
        modelo.setNumRows(0);
        for (Lote lote: ISA.loteList) {
            modelo.addRow(new Object[]{
                lote.getAssentamento(),
                lote.getResponsavel(), 
                lote.getNumParcela(), 
                lote.getContato(), 
                Arrays.toString(lote.getCoordenada())
                        .replaceAll("\\[|\\]", "")
                        .replaceAll(",", "  ")
                        .replace('.', ',')
            });
        }
    }
    
    private void atualizarCbox() {
        preencherAssentamentosSet();
        JComboBox[] cboxes = new JComboBox[]{cboxAssentamento, cboxAssentamento_editar, cboxRemoverAssentamento};
        for (var cbox : cboxes) {
            preencherCbox(cbox);
        }
    }
    
    private void preencherAssentamentosSet() {
        assentamentosSet.clear();
        for (Lote lote : ISA.loteList) {
            assentamentosSet.add(lote.getAssentamento());
        }
    }
    
    private void preencherCbox(javax.swing.JComboBox cbox) {
        cbox.removeAllItems();
        for (String a : assentamentosSet) {
            cbox.addItem(a);
        }
    }
    
    private void formatarPontoPorVirgula(javax.swing.JFormattedTextField txt) {
        txt.setText(txt.getText().replace(".", ","));
    }

